package com.revature.monday.corecollection;

public class MapInterface {

    /*
    Map interface used to represent a mapping between a key and value

    It is not a subtype of the collection interface

    It behaves differently

    Maps are perfect for use in key-value associations like dictionaries
    Perform lookups by keys or when someone wants to retrieve and update elements by keys
    Common scenarios:

    Map of error codes and their description
    Map of zip codes and cities
    Map of managers and employees
            Manager (key) is associated with a list of employees (value)
    Map of classes and students


    A Map cannot contain duplicate keys and each key can map to at most one value.
    Some implementations allow null key and null values like the HashMap and LinkedHashMap, but some do not like the TreeMap.

    The order of a map depends on the specific implementations. For example, TreeMap and LinkedHashMap have predictable orders, while HashMap does not.
     */

}
