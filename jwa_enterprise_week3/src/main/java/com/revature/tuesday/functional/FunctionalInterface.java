package com.revature.tuesday.functional;

public class FunctionalInterface {

    /*
    Introduced in Java 8, the lambda
    A lambda is an anonymous function that can be handled as a first class language citizen (means we can move them like
    other data types in methods)

    Before J8 we had to create a class for every case where encapsulation was needed
    This led to a lot of boiler plate code to defien something that did a simple function

    It is recommended to use standard functional interfaces

    Any FI should have the @FunctionalInterface annotation
    Any interface with a SAM (Single Abstract Method) is a FI

    J8s default methods are not abstract and do not count
    An FI may have multiple default methods
     */
}
