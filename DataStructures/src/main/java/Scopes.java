public class Scopes {

    public static void main(String[] args){
        /*
        Scopes are how visible a variable is
            Scope is also determined by { }
        Class level
            Something that is in class level is accessible anywhere within the same class
        Method Level
            Something that is in the method scope, is only accessible within that method
        Block level (scope)
            Within for loops or control flow
        for(int i = 0; i< 10; i++){
            System.out.println(i);
        }
         */
    }
}
